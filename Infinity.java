public class Infinity{

	public double POSITIVE_INFINITY;
	public double NEGATIVE_INFINITY;
	public double NaN;

	public static void main(String[] args){
		Infinity infinity = new Infinity();
		System.out.println(infinity.POSITIVE_INFINITY);
		System.out.println(infinity.NEGATIVE_INFINITY);
		System.out.println(infinity.NaN);
	}
}